    <h2>Geschmackssache: Banana Split, Bananentrunk und andere Bananenrezepte</h2>
    <p>Es gibt viele Banenensorten,
        zum Beispiel die Desserbananen, aber es gibt auch Kochbananen, welche eher wie Kartoffeln verwendet werden.
        Kochbananen sind eine Bananenart, die sich gut zum Kochen eignet. Bananen ohne den Hinweis auf biologische
        Bedeutung sind oft als konventionelle Bananen bezeichnet und sie sind oft mit giftigen Pestiziden belastet.

    <p>Banenwasser gilt als Schlaftrunk. Ein einfaches Rezept für Bananenwasser ist, eine Banane mit Schale in Scheiben zu
        schneiden, mit kochendem Wasser zu begiessen und ziehen zu lassen. Das Bananenwasser kann dann abgeseiht und warm
        getrunken werden, oft wird auch eine Prise Zimt zugegeben.

    <p>Zugegeben ist Banentrunk Geschmackssache und schmeckt nicht jedem. Bananen lassen sich auch braten. Dann schmecken
        sie gleich viel besser. Wer es mag, kann ganz viel Fett, Zucker und Honig hinzugeben. Aus Bananen lassen sich auch
        Speiseeisdesserts zubereiten, zum Beispiel das beliebte Bananen-Eis "Banana Split".

    <p>Mit Bananen kann Bananenbrot gebacken oder Bananen lassen sich oder als Topping von Eierkuchen verwenden.
        Weitere leckere Bananenrezepte sind schnelle Schoko-Bananen-Muffins, Bananen-Walnuss-Cookies, Schoko-Bananenbrot mit
        Joghurt oder Bananen-Curry auf seychellische Art.

