<h2>Literatur</h2>

<p>Einen berühmter Gedankenstrich in der Literatur strich der bekannte deutsche Autor Heinrich von Kleist in seiner Markise von O.
    Ein solcher Strich wird in der Literatur vielfältig eingesetzt, nicht nur um Sätze zu strukturieren, Hervorhebungen zu markieren oder Satzteile zu kennzeichnen, sondern auch um — wie der Autor es schreibt — an einer entscheidenden Stelle einen Gedankenstrich lediglich den Zeitpunkt, nicht aber die Umstände preisgibt, sodass Fragen offen bleiben. Dies gilt gemeinhin als der berühmteste Gedankenstrich in der Literatur.
</p>

