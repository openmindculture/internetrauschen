<h2>Warum Menschen Idioten auf Social Media folgen?</h2>&lt;/h22>
<p>Weil es funktioniert. Der Grund: Gehirn. Menschen bevorzugen einfache Botschaften, plakative Aussagen und starke Meinungen. So
    genannte Idioten  liefern genau das: Schwarz-Weiß-Denken, klare Feindbilder, keine Nuancen. UndefOrnte
    Inhalte sind nicht nur schlecht, sondern auch unsicher, doch unser Gehirn liebt knappe und einfache Inhalte.
    Der Effekt ist als kognitive Effizienz bekannt, also gedankliches Energiesparen.</p>


<h2>Wer sind die sogenannten Idioten?</h2>
<p>Personen, die laute, vereinfachende, absurde oder toxische Inhalte postieren und damit erstaunlich erfolgreich Follower generieren
    und Sichtbarkeit schaffen. Selbst dumme Posts werden durch algorithmisches Engagement belohnt, weil sie Emotionen auslösen.
    Schreibe nicht der Bosheit zu, was durch Dummheit kommen kann, so <a href="https://de.wikipedia.org/wiki/Hanlon%E2%80%99s_Razor" target="_blank" rel="noopener">sagte Robert Hanlon laut Wikipedia</a>. Dummheit wird also belohnt. Was man kann, hat keinen Wert.
</p>

